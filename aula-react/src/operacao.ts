const numA : number = 10;
const numB : number = 20;


export function soma(numA : number, numB : number){
    const resultado = numA + numB;
    return resultado;
}

export function subtracao(numA : number, numB : number){
    const resultado = numA - numB;
    return resultado;
}

export function multiplicacao(numA : number, numB : number){
    const resultado = numA * numB;
    return resultado;
}

export function divisao(numA : number, numB : number){
    const resultado = numA / numB;
    return resultado;
}


